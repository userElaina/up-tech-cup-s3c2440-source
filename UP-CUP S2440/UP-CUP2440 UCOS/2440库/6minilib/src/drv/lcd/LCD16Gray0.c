/***************************************************************************\
	私有的LCD初始化函数,在LCD_Init()设置LCD寄存器之后调用
	可以初始化时序等信息
\***************************************************************************/
void LCD_pInit(void)
{
	rLcdTiming0|=LcdHBP(8)|LcdHFP(8)|LcdHSW(40);
	rLcdTiming1|=LcdVBP(0)|LcdVFP(0)|LcdVSW(0);
	rLcdTiming2|=LcdCPL(LCDWIDTH/4-1)|LcdPCD(10);

}

/***************************************************************************\
	液晶屏被光开关函数
\***************************************************************************/
void LCDBkLight(int isOpen)
{
	if(isOpen)
		rLcdControl |= LcdBLEn;
	else
		rLcdControl &= (~LcdBLEn);
}

/***************************************************************************\
	液晶屏显示开关函数
\***************************************************************************/
void LCDDisplayOpen(int isOpen)
{
	if(isOpen)
		rLcdControl |= LcdPowerEn;
	else
		rLcdControl &= (~LcdPowerEn);
}

